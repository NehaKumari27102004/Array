package Arrays;

public class LearningIf {
    public static void main(String[] args){
        boolean isMale=false;
        String name = "Daya";
        System.out.println("before if");
        if(isMale){
            System.out.println("Mr."+name);
        }else{
            System.out.println("Ms." +name);
        }
        System.out.println("after if");
    }
}
